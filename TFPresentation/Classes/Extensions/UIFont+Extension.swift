import Foundation
import UIKit

public extension UIFont {
    static let regular: UIFont = UIFont.systemFont(ofSize: 16, weight: .regular)
    static let regularBold: UIFont = UIFont.systemFont(ofSize: 16, weight: .bold)
    static let subtext: UIFont = UIFont.systemFont(ofSize: 13, weight: .regular)
}
